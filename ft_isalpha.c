#include <stdio.h>

int ft_isalpha(int c)
{
	if ((c > 64 && c < 91) || (c > 96 && c < 12))
		return 1;
	else 
		return 0;
}

int main()
{
	char str[] = "12abc12";
	printf("%d\n", ft_isalpha(str[1]));
	return 0;
}
